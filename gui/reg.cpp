reg::reg(int nr, QWidget* parent) : QWidget(parent)
{
    ui_.setupUi(this);
    ui_.nr->setText(QString::number(nr));

    if(nr >= 1 && nr <= 9)
    {
        ui_.nr->setBuddy(ui_.path);
        ui_.nr->setText("&" + ui_.nr->text());
    }
}

