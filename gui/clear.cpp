////////////////////////////////////////////////////////////////////////////////
clear::clear(QWidget* parent) : QWidget(parent)
{
    ui_.setupUi(this);
    connect(ui_.play, &QToolButton::clicked, this, &clear::play);
}

////////////////////////////////////////////////////////////////////////////////
void clear::exec(src::trigger trig)
{
    if(trig == src::trigger::play) ui_.play->click();
}

