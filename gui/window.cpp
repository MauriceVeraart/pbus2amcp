#include "window.hpp"
#include "ui_window.h"

#include <QCloseEvent>

////////////////////////////////////////////////////////////////////////////////
namespace gui
{

////////////////////////////////////////////////////////////////////////////////
window::window(QWidget* parent) : QWidget(parent),
    ui(new Ui::window)
{
    ui->setupUi(this);
}

////////////////////////////////////////////////////////////////////////////////
window::~window()
{
    delete ui;
}

////////////////////////////////////////////////////////////////////////////////
void window::closeEvent(QCloseEvent* event)
{
    emit closing();
    QWidget::closeEvent(event);
}

////////////////////////////////////////////////////////////////////////////////
}
